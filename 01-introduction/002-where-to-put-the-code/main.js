/*
    There are two ways to write JS code for your project
*/

/* Script Tag */
/*
    -You can write JS code on HTML files by creating "script" tag and start writing JS code inside of them
    -Check the HTML file existing in this folder
    -Example:
        <script>
            console.log("Hello from HTML file");
        </script>
*/

/* JS file */
/*
    -You can create a JS file and link it with the HTML file you are working on
    -Example:
        <script src="main.js" ></script>
        // Note: You can link many JS files to an HTML file
        <script src="main2.js" ></script>
*/
