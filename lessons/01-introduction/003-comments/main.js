/* Comments */
/*
    -Comments in code are notes written by developers to explain what the code does
    -They are ignored by the computer when the program runs, so they don't affect how the program works
*/

/* Why use comments ? */
/*
    -To explain what a piece of code is doing
    -To remind yourself (or others) why something was done a certain way
    -To temporarily disable parts of code for testing
    -To document the code
*/

